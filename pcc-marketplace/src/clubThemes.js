export function injectClubTheme(element, clubId) {
  const theme = getClubTheme(clubId);
  if (!theme || !element) return;

  element.style.setProperty('--club-primary', theme.primaryColor);
  element.style.setProperty('--club-secondary', theme.secondaryColor);
  element.style.setProperty('--club-accent', theme.accentColor);
  element.style.setProperty('--club-dark', theme.darkColor);
  element.style.setProperty('--club-bg-gradient', theme.bgGradient);
  element.style.setProperty('--club-card-gradient', theme.cardGradient);
  element.style.setProperty('--club-hero-gradient', theme.heroGradient);
  element.style.setProperty('--club-glow', theme.glowColor);
  element.style.setProperty('--club-glow-strong', theme.glowColorStrong);
  element.style.setProperty('--club-text', theme.textOnBg);
}
