// Helper to get theme by club ID
export function getClubTheme(clubId) {
  return CLUB_THEMES[clubId] || null;
}

// Get all clubs as an array
export function getAllClubs() {
  return Object.values(CLUB_THEMES);
}

// Get featured club
export function getFeaturedClub() {
  return Object.values(CLUB_THEMES).find(c => c.featured) || Object.values(CLUB_THEMES)[0];
}

